addi $2, $0, 2		# $2 = 2
addi $3, $0, -3		# $3 = -3
addi $4, $0, 4		# $4 = 4

addi $5, $0, 100	# $5 = 100
addi $6, $0, 104	# $6 = 104
addi $7, $0, 108	# $7 = 108

sw $2, 0($5)		# Mem[100] = 2
sw $3, 0($6)		# Mem[104] = -3
sw $4, 0($7)		# Mem[108] = 4

xchg $5, $6		# Mem[100] = -3, Mem[104] = 2
xchg $6, $7		# Mem[104] = 4,  Mem[108] = 2

lw $2, 0($5)		# $2 = -3
lw $3, 0($6)		# $3 = 4
lw $4, 0($7)		# $4 = 2